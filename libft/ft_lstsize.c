#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*p;
	int		i;

	i = 0;
	p = NULL;
	p = lst;
	while (p)
	{
		i++;
		p = p -> next;
	}
	return (i);
}
